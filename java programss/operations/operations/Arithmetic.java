class arithmetic
{
    public static void main(String [] args)
    {
        int a=20,b=30,c,d,e,f,g;
        c=a+b;
        d=a-b;
        e=a*b;
        f=a/b;
        g=a%b;
        System.out.println("Arithmetic operations on "+a+" and "+b+"are:");
        System.out.println("Sum of "+a+" and "+b+" is:"+c);
        System.out.println("Difference between "+a+" and "+b+" is:"+d);
        System.out.println("Product of "+a+" and "+b+" is:"+e);
        System.out.println("Quotient of "+a+" when divided by "+b+" is:"+f);
        System.out.print("Remainder of "+a+" when divided by "+b+" is:"+g);
    }
}
