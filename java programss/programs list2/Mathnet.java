class math
{
public static void main(String [] args)
{
System.out.println(Math.floor(3.4));
System.out.println("Ceil function:"+Math.ceil(3.4));
System.out.println("Floor function:"+Math.round(3.4));
System.out.println("Power function:"+Math.pow(3,4));
System.out.println("Squareroot function:"+Math.sqrt(34));
System.out.println("cuberoot function:"+Math.cbrt(34));
System.out.println("Maximum function:"+Math.max(3,4));
System.out.println("Minimum function:"+Math.min(3,4));
System.out.println("Sign num function:"+Math.signum(-34));
System.out.println("Hypotenuse function:"+Math.hypot(3,4));
System.out.println("absolute function:"+Math.abs(-3.4));
System.out.println("Random function:"+Math.random()+3);
System.out.println("Nextup function:"+Math.nextUp(3.4));
System.out.println("IEEE remainder function:"+Math.IEEEremainder(3,-4));
System.out.println("to radians function:"+Math.toRadians(3.4));
System.out.println("to degrees function:"+Math.toDegrees(3.34));
}
}